#include "3-calc.h"

/**
 * op_add - add 2 no.
 *
 * @a: first no.
 * @b: second no.
 *
 * Return: sum
 *
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub 2 number
 *
 * @a: fiest no.
 * @b: second no.
 *
 * Return: sub
 *
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mult 2 no.
 *
 * @a: fiest no.
 * @b: second no.
 *
 * Return: mult
 *
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div 2 no.
 *
 * @a: first in.
 * @b: second no.
 *
 * Return: div
 *
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod 2 no.
 *
 * @a: first no.
 * @b: second no.
 *
 * Return: mod
 *
 */

int op_mod(int a, int b)
{
	return (a % b);
}
